    <p>DoubleKey aims to complete it by the time you finished typing &lt;!DO.</p>
    <h3>How does it work?</h3>
    <p>You're probably familiar with Alt+Tab, a key combination where you hold 
        the Alt key and press on Tab to see other windows.</p>
    <p>Similarly, DoubleKey configures -any- of your keyboard keys to be 
        shortcuts. DoubleKey has the potential to</p>
    <ul>
        <li>If you hold &quot;H&quot; and press &quot;E&quot;, you can get &quot;Hello World!&quot; (if you 
            want to)</li>
        <li>If you hold &quot;D&quot; and press &quot;E&quot;, you can get &quot;deviantart.com&quot; (if you 
            want to).</li>
    </ul>
