    <h3>What is DoubleKey?</h3>
    <p>DoubleKey is a text input accelerator for Windows that helps instantly 
        complete your text.</p>
    <p>For example, if you want to type a complex &quot;sentence&quot;:</p>
    <p style="text-align:center">&lt;!DOCTYPE html PUBLIC &quot;-//W3C//DTD XHTML 1.1//EN&quot; 
